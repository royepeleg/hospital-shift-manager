/** A family member who can be assigned to events. */
export interface FamilyMember {
  id: string;
  name: string;
  /** Hex color string (e.g. '#ff0000') used to visually identify this member. */
  color: string;
  phone?: string;
}

/** The type of a calendar event. */
export type EventType =
  | 'shift'
  | 'treatment'
  | 'visit'
  | 'parent-note'
  | 'parent-coverage'
  | 'gap';

